/** Pseudo Code */

// Enter Two Numbers


export function Check() {
   
    let $Num1 = parseInt(prompt("Enter First Number:"));
    let $Num2 = parseInt(prompt("Enter Second Number :"))
    // Check if one of them equals 100 or sum of Numbers are 100
    if ($Num1 == 100) {
        alert("Number One equals 100");
    } else if ($Num2 == 100) {
        alert("Number Two equals 100");
    } else if ($Num1 + $Num2 == 100) {
        alert(" Sum of Two  Numbers are equaling 100");
    } else {
        alert("Invalid Two Numbers Try Again...!!");
    }
}
